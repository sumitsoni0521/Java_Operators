public class arthmaticOperators {
	public static void main(String[] args) {
		System.out.println("Arithmetic Operators");
		int a = 10;
		int b = 2;
		System.out.println("a + b : " + (a + b));
		System.out.println("a - b : " + (a - b));
		System.out.println("a * b : " + (a * b));
		System.out.println("a / b : " + (a / b));
		System.out.println("a % b : " + (a % b));
	}
}
